/* ========== 主动回忆：揭示选项 ========== */
function revealWithConfidence(confidence) {
    activeRecallRevealed = true;
    // 记录置信度（用于元认知分析）
    const q = filteredQuiz[currentQuizIndex];
    if (!spacedRepetition.items[q.id]) {
        spacedRepetition.items[q.id] = {
            interval: 1,
            ease: 2.5,
            history: [],
            nextReview: Date.now() + 24 * 60 * 60 * 1000,
            lastConfidence: confidence
        };
    } else {
        spacedRepetition.items[q.id].lastConfidence = confidence;
    }
    renderQuiz();
    // 揭示后滚动到题目卡片顶部
    requestAnimationFrame(() => {
        const card = document.querySelector('.quiz-card');
        if (card) card.scrollIntoView({ behavior: 'smooth', block: 'start' });
    });
}

