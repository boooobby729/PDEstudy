function saveProgress() {
    const data = {
        quizAnswered,
        correctCount,
        completedCards: Array.from(completedCards),
        spacedRepetition: spacedRepetition.items,
        elaborations,
        sessionStats: {
            // 只持久化累计数据，不持久化当次会话时间
            totalAnswered: (JSON.parse(localStorage.getItem('pxdlab_progress') || '{}').sessionStats || {}).totalAnswered || 0
        }
    };
    try {
        localStorage.setItem('pxdlab_progress', JSON.stringify(data));
    } catch (e) { /* ignore */ }
    updateGlobalProgress();
}

function loadProgress() {
    try {
        const raw = localStorage.getItem('pxdlab_progress');
        if (raw) {
            const data = JSON.parse(raw);
            quizAnswered = data.quizAnswered || {};
            correctCount = data.correctCount || 0;
            completedCards = new Set(data.completedCards || []);
            if (data.spacedRepetition) {
                spacedRepetition.items = data.spacedRepetition;
            }
            if (data.elaborations) {
                elaborations = data.elaborations;
            }
            updateQuizScore();
        }
    } catch (e) { /* ignore */ }
    updateGlobalProgress();
}

function updateGlobalProgress() {
    const total = quizData.length;
    const done = Object.keys(quizAnswered).length;
    const pct = total > 0 ? Math.round((done / total) * 100) : 0;
    const bar = document.getElementById('globalProgress');
    const text = document.getElementById('globalProgressText');
    if (bar) bar.style.width = pct + '%';
    if (text) text.textContent = pct + '%';

    // 更新首页统计数字
    const statTotal = document.getElementById('statTotalQuestions');
    if (statTotal) statTotal.textContent = total;
    const statDone = document.getElementById('statAnswered');
    if (statDone) statDone.textContent = done;
}

