/* ========== 间隔重复系统（SM-2 变体） ========== */
let spacedRepetition = {
    // { questionId: { nextReview: timestamp, interval: days, ease: factor, history: [{date, correct}] } }
    items: {},
    reviewQueue: []
};

function getReviewItems() {
    const now = Date.now();
    return Object.entries(spacedRepetition.items)
        .filter(([id, item]) => item.nextReview <= now)
        .map(([id]) => parseInt(id));
}

function scheduleReview(questionId, correct) {
    const item = spacedRepetition.items[questionId] || { interval: 1, ease: 2.5, history: [] };
    if (correct) {
        item.interval = Math.round(item.interval * item.ease);
        item.ease = Math.min(3.0, item.ease + 0.1);
    } else {
        item.interval = 1;
        item.ease = Math.max(1.3, item.ease - 0.2);
    }
    item.nextReview = Date.now() + item.interval * 24 * 60 * 60 * 1000;
    if (!item.history) item.history = [];
    item.history.push({ date: Date.now(), correct });
    spacedRepetition.items[questionId] = item;
}

/* ========== 交错练习：生成混合题序 ========== */
function generateInterleavedOrder(questions) {
    // 按类别分组，然后交替抽取，确保相邻题目类别不同
    const groups = {};
    questions.forEach(q => {
        if (!groups[q.category]) groups[q.category] = [];
        groups[q.category].push(q);
    });
    const cats = Object.keys(groups);
    // 每组内部随机打乱
    cats.forEach(c => {
        for (let i = groups[c].length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [groups[c][i], groups[c][j]] = [groups[c][j], groups[c][i]];
        }
    });
    // 轮询各类别交替取题
    const result = [];
    let maxLen = Math.max(...cats.map(c => groups[c].length));
    for (let i = 0; i < maxLen; i++) {
        cats.forEach(c => {
            if (groups[c][i]) result.push(groups[c][i]);
        });
    }
    return result;
}

/* ========== 元认知：学习效率分析 ========== */
function getMetacognitiveInsights() {
    const allAnswered = Object.entries(quizAnswered);
    if (allAnswered.length === 0) return null;

    // 按类别统计正确率
    const catStats = {};
    quizData.forEach(q => {
        if (!catStats[q.category]) catStats[q.category] = { total: 0, correct: 0, name: getCatName(q.category) };
        if (quizAnswered[q.id] !== undefined) {
            catStats[q.category].total++;
            if (quizAnswered[q.id] === q.answer) catStats[q.category].correct++;
        }
    });

    // 找出最弱项
    let weakest = null, weakestRate = 1;
    Object.entries(catStats).forEach(([cat, s]) => {
        if (s.total > 0) {
            const rate = s.correct / s.total;
            if (rate < weakestRate) { weakestRate = rate; weakest = { cat, ...s }; }
        }
    });

    // 遗忘风险：距上次答题时间越长、正确率越低，风险越高
    const forgettingRisk = [];
    Object.entries(spacedRepetition.items).forEach(([id, item]) => {
        const q = quizData.find(q => q.id === parseInt(id));
        if (!q) return;
        const daysSince = (Date.now() - (item.nextReview - item.interval * 86400000)) / 86400000;
        const risk = Math.min(1, daysSince / (item.interval * 1.5)) * (1 / item.ease);
        forgettingRisk.push({ id: parseInt(id), q, risk, interval: item.interval, ease: item.ease });
    });
    forgettingRisk.sort((a, b) => b.risk - a.risk);

    return { catStats, weakest, forgettingRisk: forgettingRisk.slice(0, 5) };
}

function getCatName(cat) {
    return {
        product: '产品思维', interaction: '交互设计', methodology: '方法论',
        'ix-live': '直播场景', 'ix-bside': 'B端设计', 'meth-ai': 'AI新范式'
    }[cat] || cat;
}

/* ========== 遗忘曲线可视化数据 ========== */
function getForgettingCurveData(questionId) {
    const item = spacedRepetition.items[questionId];
    if (!item || !item.history) return null;
    // 基于 Ebbinghaus 遗忘曲线：R = e^(-t/S)，S 由 ease 决定
    const points = [];
    const S = item.ease * 2; // 稳定性参数
    for (let day = 0; day <= item.interval * 2; day++) {
        const retention = Math.exp(-day / S) * 100;
        points.push({ day, retention: Math.max(0, retention) });
    }
    return points;
}

