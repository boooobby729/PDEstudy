/* ── 综合评价模块 ── */
function buildOverallEvaluation(totalAnswered, accuracy, wrongCount, elaborationCount, reviewDue) {
    if (totalAnswered < 5) {
        return `<div class="eval-card neutral">
            <div class="eval-grade">🌱</div>
            <div class="eval-content">
                <strong>学习刚刚起步</strong>
                <p>再答 ${5 - totalAnswered} 道题，系统就能为你生成精准的学习评价。</p>
            </div>
        </div>`;
    }

    let grade, gradeLabel, gradeColor, summary;
    const score = accuracy * 0.5 + (elaborationCount / Math.max(totalAnswered, 1)) * 30 + (wrongCount === 0 ? 20 : Math.max(0, 20 - wrongCount));

    if (score >= 75) { grade = 'A'; gradeLabel = '优秀'; gradeColor = '#4CAF50'; }
    else if (score >= 55) { grade = 'B'; gradeLabel = '良好'; gradeColor = '#2196F3'; }
    else if (score >= 35) { grade = 'C'; gradeLabel = '进步中'; gradeColor = '#FF9800'; }
    else { grade = 'D'; gradeLabel = '需加强'; gradeColor = '#F44336'; }

    const strengths = [];
    const weaknesses = [];
    if (accuracy >= 75) strengths.push('答题正确率高');
    else weaknesses.push(`正确率 ${accuracy}%，还有提升空间`);
    if (elaborationCount >= totalAnswered * 0.4) strengths.push('深度反思习惯好');
    else weaknesses.push('深度追问参与度低，建议多用自己的话解释答案');
    if (reviewDue === 0) strengths.push('复习计划执行到位');
    else weaknesses.push(`${reviewDue} 道题待复习，建议尽快完成`);
    if (wrongCount <= 2) strengths.push('错题数量少');
    else weaknesses.push(`${wrongCount} 道错题需要重点复习`);

    return `<div class="eval-card">
        <div class="eval-grade" style="color:${gradeColor}">${grade}</div>
        <div class="eval-content">
            <strong>${gradeLabel} · 综合学习评分 ${Math.round(score)}/100</strong>
            ${strengths.length > 0 ? `<p class="eval-strengths">✅ ${strengths.join('、')}</p>` : ''}
            ${weaknesses.length > 0 ? `<p class="eval-weaknesses">📌 ${weaknesses.join('；')}</p>` : ''}
        </div>
    </div>`;
}

/* ── 活跃度热力图（近 28 天） ── */
function buildActivityHeatmap() {
    // 从 quizAnswered 中提取答题时间戳（如有记录）
    // 由于现有数据结构没有时间戳，用 spacedRepetition 的 lastReview 推算
    const dayMap = {};
    Object.values(spacedRepetition.items).forEach(item => {
        if (item.lastReview) {
            const d = new Date(item.lastReview);
            const key = `${d.getFullYear()}-${d.getMonth()}-${d.getDate()}`;
            dayMap[key] = (dayMap[key] || 0) + 1;
        }
    });

    const today = new Date();
    const cells = [];
    const weekLabels = ['日', '一', '二', '三', '四', '五', '六'];

    // 生成 28 天格子（4 周）
    for (let i = 27; i >= 0; i--) {
        const d = new Date(today);
        d.setDate(today.getDate() - i);
        const key = `${d.getFullYear()}-${d.getMonth()}-${d.getDate()}`;
        const count = dayMap[key] || 0;
        const level = count === 0 ? 0 : count <= 2 ? 1 : count <= 5 ? 2 : count <= 10 ? 3 : 4;
        const isToday = i === 0;
        const label = `${d.getMonth() + 1}/${d.getDate()} 答了 ${count} 题`;
        cells.push(`<div class="heat-cell level-${level}${isToday ? ' today' : ''}" title="${label}"></div>`);
    }

    const maxCount = Math.max(...Object.values(dayMap), 0);
    return `
        <div class="heatmap-grid">${cells.join('')}</div>
        <div class="heatmap-legend">
            <span>少</span>
            <div class="heat-cell level-0"></div>
            <div class="heat-cell level-1"></div>
            <div class="heat-cell level-2"></div>
            <div class="heat-cell level-3"></div>
            <div class="heat-cell level-4"></div>
            <span>多</span>
            ${maxCount > 0 ? `<span class="heatmap-max">最多 ${maxCount} 题/天</span>` : ''}
        </div>`;
}

/* ── SVG 雷达图 ── */
function renderRadarChart(catEntries) {
    const svg = document.getElementById('radarChart');
    if (!svg || catEntries.length < 3) return;

    const cx = 130, cy = 110, R = 80;
    const n = catEntries.length;
    const levels = 4;

    // 计算各顶点坐标
    const angle = (i) => (Math.PI * 2 * i / n) - Math.PI / 2;
    const pt = (i, r) => ({
        x: cx + r * Math.cos(angle(i)),
        y: cy + r * Math.sin(angle(i))
    });

    // 背景网格
    let gridLines = '';
    for (let l = 1; l <= levels; l++) {
        const r = R * l / levels;
        const pts = Array.from({ length: n }, (_, i) => pt(i, r));
        gridLines += `<polygon points="${pts.map(p => `${p.x},${p.y}`).join(' ')}"
            fill="none" stroke="#e0e0e0" stroke-width="1"/>`;
    }

    // 轴线
    let axes = '';
    catEntries.forEach((_, i) => {
        const p = pt(i, R);
        axes += `<line x1="${cx}" y1="${cy}" x2="${p.x}" y2="${p.y}" stroke="#e0e0e0" stroke-width="1"/>`;
    });

    // 数据多边形
    const dataPoints = catEntries.map(([, s], i) => {
        const pct = s.total > 0 ? s.correct / s.total : 0;
        return pt(i, R * pct);
    });
    const dataPolygon = `<polygon points="${dataPoints.map(p => `${p.x},${p.y}`).join(' ')}"
        fill="rgba(108,99,255,0.2)" stroke="#6C63FF" stroke-width="2"/>`;

    // 数据点
    const dots = dataPoints.map((p, i) => {
        const pct = catEntries[i][1].total > 0
            ? Math.round(catEntries[i][1].correct / catEntries[i][1].total * 100) : 0;
        return `<circle cx="${p.x}" cy="${p.y}" r="4" fill="#6C63FF"/>
            <title>${catEntries[i][1].name}: ${pct}%</title>`;
    }).join('');

    // 标签
    const labels = catEntries.map(([, s], i) => {
        const p = pt(i, R + 22);
        const pct = s.total > 0 ? Math.round(s.correct / s.total * 100) : 0;
        const color = pct >= 70 ? '#4CAF50' : pct >= 40 ? '#FF9800' : '#F44336';
        return `<text x="${p.x}" y="${p.y}" text-anchor="middle" dominant-baseline="middle"
            font-size="10" fill="${color}" font-weight="500">${s.name.substring(0, 4)}</text>`;
    }).join('');

    svg.innerHTML = gridLines + axes + dataPolygon + dots + labels;
}

/* ── 跳转到指定题目 ── */
function jumpToQuestion(qId) {
    const q = quizData.find(q => q.id === qId);
    if (!q) return;
    // 关闭仪表盘
    const dash = document.getElementById('metacogDashboard');
    if (dash) dash.style.display = 'none';
    // 切换到题库页
    showPage('quiz');
    // 切换到对应分类
    currentCategory = q.category;
    currentQuizIndex = 0;
    // 找到该题在分类中的索引
    const catQuestions = quizData.filter(q2 => q2.category === q.category);
    const idx = catQuestions.findIndex(q2 => q2.id === qId);
    if (idx >= 0) currentQuizIndex = idx;
    renderQuiz();
    // 更新分类按钮状态
    document.querySelectorAll('.category-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.category === q.category);
    });
}

function generateLearningAdvice(insights, totalAnswered, accuracy, wrongItems) {
    const advice = [];
    wrongItems = wrongItems || [];

    if (totalAnswered < 5) {
        advice.push({
            type: 'info', icon: '🚀',
            text: `先完成 ${5 - totalAnswered} 道题，系统就能为你生成精准的学习分析。`,
            action: { label: '去答题', fn: "showPage('quiz')" }
        });
        return advice;
    }

    // 1. 待复习提醒（最高优先级）
    const reviewCount = getReviewItems().length;
    if (reviewCount > 0) {
        advice.push({
            type: 'urgent', icon: '⏰',
            text: `有 ${reviewCount} 道题到了最佳复习时间窗口！趁记忆还在边缘时强化，效果是普通复习的 3 倍。`,
            action: { label: '立即复习', fn: "setLearningMode('spaced');document.getElementById('metacogDashboard').style.display='none'" }
        });
    }

    // 2. 薄弱类别专项练习
    if (insights.weakest && insights.weakest.total > 2) {
        const rate = Math.round(insights.weakest.correct / insights.weakest.total * 100);
        advice.push({
            type: 'warning', icon: '🎯',
            text: `「${insights.weakest.name}」是你的薄弱项（正确率仅 ${rate}%，答了 ${insights.weakest.total} 题）。建议切换到该类别，用「主动回忆」模式专项突破。`,
            action: { label: '专项练习', fn: `switchToWeakCategory('${insights.weakest.cat || ''}')` }
        });
    }

    // 3. 错题针对性建议
    if (wrongItems.length >= 3) {
        // 分析错题集中的类别
        const wrongCatCount = {};
        wrongItems.forEach(q => {
            wrongCatCount[q.category] = (wrongCatCount[q.category] || 0) + 1;
        });
        const topWrongCat = Object.entries(wrongCatCount).sort((a, b) => b[1] - a[1])[0];
        const catName = insights.catStats[topWrongCat[0]] ? insights.catStats[topWrongCat[0]].name : topWrongCat[0];
        advice.push({
            type: 'warning', icon: '❌',
            text: `你有 ${wrongItems.length} 道错题，其中「${catName}」错误最多（${topWrongCat[1]} 道）。错题是最宝贵的学习资源——每道错题都指向一个认知盲区。`,
            action: { label: '查看错题', fn: "document.querySelector('.wrong-list').scrollIntoView({behavior:'smooth'})" }
        });
    }

    // 4. 深度反思建议
    const elaborationCount = Object.keys(elaborations).filter(k => elaborations[k]).length;
    if (totalAnswered > 8 && elaborationCount < totalAnswered * 0.25) {
        advice.push({
            type: 'tip', icon: '✍️',
            text: `答题后的「深度理解追问」你只完成了 ${elaborationCount} 次（${Math.round(elaborationCount / totalAnswered * 100)}%）。研究表明，用自己的话解释答案能将记忆保留率提升 40%+，这是最高效的学习方式之一。`
        });
    }

    // 5. 交错练习建议（高正确率时）
    if (accuracy >= 75 && totalAnswered >= 15) {
        advice.push({
            type: 'success', icon: '🔀',
            text: `正确率 ${accuracy}%，表现很好！试试「交错练习」模式——混合不同类别能检验你是否真正理解，而非只是熟悉题目顺序。`,
            action: { label: '交错练习', fn: "setLearningMode('interleaved');document.getElementById('metacogDashboard').style.display='none'" }
        });
    }

    // 6. 进度激励
    const totalQ = quizData.length;
    const pct = Math.round(totalAnswered / totalQ * 100);
    if (pct < 30) {
        advice.push({
            type: 'info', icon: '📚',
            text: `你已完成 ${pct}% 的题目（${totalAnswered}/${totalQ}）。坚持每天 10 道题，${Math.ceil((totalQ - totalAnswered) / 10)} 天后就能刷完全部题库！`
        });
    } else if (pct >= 80) {
        advice.push({
            type: 'success', icon: '🏆',
            text: `已完成 ${pct}% 的题目，非常棒！现在是巩固阶段——重点用间隔复习强化已学内容，让知识真正进入长期记忆。`
        });
    }

    // 7. 兜底建议
    if (advice.length === 0) {
        advice.push({
            type: 'success', icon: '⭐',
            text: '学习状态很好！保持每天练习的习惯，间隔重复的效果会在几周后显现。记住：学习的关键不是一次性突击，而是持续的间隔强化。'
        });
    }

    return advice;
}

