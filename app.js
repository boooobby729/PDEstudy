/* ========== 遗忘曲线图表（纯 CSS/SVG 实现） ========== */
function renderForgettingCurveChart() {
    const container = document.getElementById('forgettingCurveChart');
    if (!container) return;

    // 取最近答过的5道题绘制曲线
    const recentIds = Object.keys(spacedRepetition.items).slice(-5).map(Number);
    if (recentIds.length === 0) {
        container.innerHTML = '<p class="metacog-tip">答题后这里会显示你的记忆保留曲线</p>';
        return;
    }

    const W = 400, H = 160;
    const colors = ['#6C63FF', '#4ECDC4', '#FF6B6B', '#45B7D1', '#96CEB4'];

    let paths = '';
    let legend = '';

    recentIds.forEach((id, idx) => {
        const item = spacedRepetition.items[id];
        if (!item) return;
        const q = quizData.find(q => q.id === id);
        const S = item.ease * 2;
        const maxDay = Math.max(14, item.interval * 2);
        const color = colors[idx % colors.length];

        // 生成 SVG path
        let d = '';
        for (let day = 0; day <= maxDay; day++) {
            const x = (day / maxDay) * (W - 40) + 20;
            const retention = Math.exp(-day / S);
            const y = H - 20 - retention * (H - 40);
            d += day === 0 ? `M ${x} ${y}` : ` L ${x} ${y}`;
        }

        // 标记当前位置（今天）
        const todayRetention = Math.exp(-0 / S);
        const todayX = 20;
        const todayY = H - 20 - todayRetention * (H - 40);

        paths += `<path d="${d}" stroke="${color}" stroke-width="2" fill="none" opacity="0.8"/>
            <circle cx="${todayX}" cy="${todayY}" r="4" fill="${color}"/>`;

        if (q) {
            legend += `<span class="curve-legend-item" style="color:${color}">
                ● ${q.question.substring(0, 20)}...
            </span>`;
        }
    });

    // Y轴标签
    const yLabels = [100, 75, 50, 25, 0].map(v => {
        const y = H - 20 - (v / 100) * (H - 40);
        return `<text x="14" y="${y + 4}" font-size="9" fill="#999" text-anchor="end">${v}%</text>
            <line x1="18" y1="${y}" x2="${W - 10}" y2="${y}" stroke="#eee" stroke-width="1"/>`;
    }).join('');

    container.innerHTML = `
        <svg viewBox="0 0 ${W} ${H}" class="forgetting-svg">
            ${yLabels}
            <text x="${W / 2}" y="${H - 2}" font-size="9" fill="#999" text-anchor="middle">天数 →</text>
            ${paths}
        </svg>
        <div class="curve-legend">${legend}</div>
    `;
}

