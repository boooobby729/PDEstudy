function initCases() {
    if (typeof caseStudies === 'undefined') return;

    // 筛选按钮
    document.querySelectorAll('.case-filter-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            document.querySelectorAll('.case-filter-btn').forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            currentCaseFilter = btn.dataset.caseCat;
            renderCases();
        });
    });

    // 搜索
    const searchInput = document.getElementById('caseSearchInput');
    if (searchInput) {
        let debounceTimer;
        searchInput.addEventListener('input', () => {
            clearTimeout(debounceTimer);
            debounceTimer = setTimeout(() => {
                currentCaseSearch = searchInput.value.trim().toLowerCase();
                renderCases();
            }, 200);
        });
    }

    renderCases();
}

function getFilteredCases() {
    let cases = caseStudies;

    // 分类筛选
    if (currentCaseFilter !== 'all') {
        cases = cases.filter(c => c.category === currentCaseFilter);
    }

    // 关键词搜索
    if (currentCaseSearch) {
        const kw = currentCaseSearch;
        cases = cases.filter(c => {
            return c.title.toLowerCase().includes(kw)
                || c.company.toLowerCase().includes(kw)
                || (c.tags && c.tags.some(t => t.toLowerCase().includes(kw)))
                || (c.model && c.model.framework && c.model.framework.toLowerCase().includes(kw))
                || (c.model && c.model.reasoning && c.model.reasoning.toLowerCase().includes(kw))
                || (c.insight && c.insight.toLowerCase().includes(kw))
                || (c.category && caseCategoryNames[c.category] && caseCategoryNames[c.category].includes(kw));
        });
    }

    return cases;
}

